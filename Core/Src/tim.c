void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim)
{
	/* USER CODE BEGIN Callback 0 */
//	BaseType_t xHigherPriorityTaskWoken = pdFALSE;
	/* USER CODE END Callback 0 */
	/* USER CODE BEGIN Callback 1 */

	static uint16_t t1msTim1 = 0;
	static uint16_t t10msTim2 = 0;
	static uint16_t t1sTim4 = 0;
	static uint16_t t12_100msCnt = 0;
	static uint16_t t50msTim5_Pca = 0;

	switch ((uint32_t) htim->Instance) {
	case (uint32_t)TIM1:	/* RTC 1ms */
		RTC_ApplyToDayF( &toDayTime);
		break;
	case (uint32_t)TIM2:	/* door long Length */
		INT_TIM2HandlerProc();
		break;
	case (uint32_t)TIM3: 	/* 485 1ms Delay  */
		t1msTim3_485++;
		if( 3 == t1msTim3_485) {   // 3 ms
			if( gGoVect == 1)
				ConsoleCommProcess();//
			t1msTim3_485 = 0;
		}
		break;
	case (uint32_t)TIM4: 	/* HTTPC heartbeat  */
//		t1sTim4++;
//		if( t1sTim4 >= 2)
//		{
			flag_sent_http_request = ENABLE;
//			t1sTim4 = 0;
//		}
		break;
	case (uint32_t)TIM5:	/* PCALED Run  */
		t50msTim5_Pca++;
		if( t50msTim5_Pca > 3)
		{
			PCALED_TIM2HandlerProc();
			t50msTim5_Pca = 0;
		}
		break;
	case (uint32_t)TIM6:	/* Network Response timeout */
			t50msTim6_Net++;
			t50msTim6_Tcp++;
		break;
	case (uint32_t)TIM7:	/* My Delay timer */
	    if (delay_ticks > 0) {
			delay_ticks--;
		}
		break;
	default:
		break;
	}
}

